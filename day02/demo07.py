# 1. 算数运算符 + - * /  //  %  幂运算**
#       作用在数值之间
data01 = 5
data02 = 2
print(data01 + data02)  # 7
print("结果是：" + str(data01 + data02))  # 结果是：7
print(data01 / data02)  # 5/2 ---> 2.5
print(data01 // data02)  # 5//2 ---> 2
print(data01 % data02)  # 5 % 2 ---> 1
print(data01 ** data02)  # 5 ** 2 ---> 5的2次方


