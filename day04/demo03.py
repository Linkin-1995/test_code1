"""
    while 循环 - 循环计数
    语法：
        count = 0  # 循环前 -- 开始值
        while count  <  5: # 循环条件 -- 结束值
            循环体
            count += 1 # 循环体 -- 间隔
    作用：
        固定次数的重复

    练习:exercise02~04
"""
# 执行5次
count = 0
while count < 5:
    # print("跑圈")
    print(count)
    count += 1  # 0  1  2  3  4
