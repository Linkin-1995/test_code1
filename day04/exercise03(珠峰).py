"""
    一张纸的厚度是0.01毫米
     请计算，对折多少次超过珠穆朗玛峰(8844.43米).
"""
# 数据
#     厚度  次数   珠穆朗玛峰高度8844.43
# 运算
#     厚度 *= 2
#     次数 += 1

# thickness = 0.01  / 1000
# thickness = 0.00001
thickness = 1e-5
count = 0  # -- 循环前   定义初始值
# 如果厚度不超过珠穆朗玛峰 则对折
while thickness < 8844.43:
    # 对折
    thickness *= 2
    count += 1  # -- 循环中  累加
# -- 循环后 获取结果
print("对折" + str(count) + "次")

