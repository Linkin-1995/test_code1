import copy

list01 = ["北京", ["上海", "深圳"]]
list02 = list01  # 不是拷贝,是赋值(数据只有一份)
list03 = list01[:]  # 浅拷贝(第一层数据有两份,深层数据只有一份)
list04 = copy.deepcopy(list01)  # 深拷贝(所有数据有两份)
list04[0] = "北京04"
list04[1][1] = "深圳04"
print(list01)  # ['北京', ['上海', '深圳']]
list03[0] = "北京03"  # 修改第一层(互不影响)
list03[1][1] = "深圳03"  # 修改第二层(互相影响)
print(list01)  # ['北京', ['上海', '深圳03']]
list02[0] = "北京02"
list02[1][1] = "深圳02"
print(list01)  # ['北京02', ['上海', '深圳02']]
